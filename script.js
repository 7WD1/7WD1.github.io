// ===== Counter Animation =====
function easeOutExpo(t) {
  return t === 1 ? 1 : 1 - Math.pow(2, -10 * t);
}

function animateCounter(element, target, duration = 1500) {
  const start = 0;
  const startTime = performance.now();

  function update(currentTime) {
    const elapsed = currentTime - startTime;
    const progress = Math.min(elapsed / duration, 1);
    const easedProgress = easeOutExpo(progress);
    const current = Math.floor(start + (target - start) * easedProgress);
    element.textContent = current.toLocaleString();
    if (progress < 1) {
      requestAnimationFrame(update);
    } else {
      element.textContent = target.toLocaleString();
    }
  }

  requestAnimationFrame(update);
}

// Click handler — only for the clickable Publications stat card
const clickableCard = document.querySelector('.stat-card.clickable');
if (clickableCard) {
  clickableCard.addEventListener('click', () => {
    const numberEl = clickableCard.querySelector('.stat-number');
    const target = parseInt(clickableCard.getAttribute('data-target'), 10);

    // Add ripple effect
    const ripple = document.createElement('span');
    ripple.className = 'ripple';
    clickableCard.appendChild(ripple);
    ripple.addEventListener('animationend', () => ripple.remove());

    // Reset and animate from 0
    numberEl.textContent = '0';
    animateCounter(numberEl, target);
  });
}

// ===== Render Publications =====
function highlightAuthor(authors) {
  return authors.replace(
    /(W\.?-?D\.?\s*Jiang|Wendong Jiang|Wen-Dong Jiang)/gi,
    '<span class="highlight">$1</span>'
  );
}

function buildPubMeta(pub) {
  const parts = [];
  if (pub.vol) parts.push(`vol. ${pub.vol}`);
  if (pub.no) parts.push(`no. ${pub.no}`);
  if (pub.pages) parts.push(`pp. ${pub.pages}`);
  return parts.join(', ');
}

function buildPubDate(pub) {
  return [pub.month, pub.year].filter(Boolean).join(' ');
}

function createPubCard(pub) {
  // JCR Q1 tag (no CCF/CAA labels)
  let jcrTagHtml = '';
  if (pub.jcr === 'Q1') {
    jcrTagHtml = '<span class="jcr-tag jcr-q1-shimmer">JCR Q1</span>';
  }

  const metaParts = [];
  const metaDetail = buildPubMeta(pub);
  if (metaDetail) metaParts.push(metaDetail);
  metaParts.push(buildPubDate(pub));
  if (pub.note) metaParts.push(`<span class="pub-note">(${pub.note})</span>`);

  let linksHtml = '';
  if (pub.doi) {
    const href = pub.doi.startsWith('10.') ? `https://doi.org/${pub.doi}` : pub.doi;
    linksHtml += `<a href="${href}" class="pub-link" target="_blank" rel="noopener">DOI</a>`;
  }
  if (pub.arxiv) {
    linksHtml += `<a href="https://arxiv.org/abs/${pub.arxiv}" class="pub-link" target="_blank" rel="noopener">arXiv:${pub.arxiv}</a>`;
  }
  if (pub.url) {
    linksHtml += `<a href="${pub.url}" class="pub-link" target="_blank" rel="noopener">Link</a>`;
  }

  const typeLabel = {
    journal: 'Journal',
    conference: 'Conference',
  }[pub.type] || '';

  return `
    <div class="pub-card" data-type="${pub.type}" data-jcr="${pub.jcr || ''}">
      <div class="pub-card-header">
        <div class="pub-title">${pub.title}</div>
        <div class="pub-tags">
          <span class="pub-type-tag pub-type-${pub.type}">${typeLabel}</span>
          ${jcrTagHtml}
        </div>
      </div>
      <div class="pub-authors">${highlightAuthor(pub.authors)}</div>
      <div class="pub-venue">${pub.venue}${metaDetail ? ', ' + metaDetail : ''}, ${buildPubDate(pub)}${pub.note ? ' <span class="pub-note">(' + pub.note + ')</span>' : ''}</div>
      ${pub.if ? `<div class="pub-if">SCI Impact Factor: ${pub.if}</div>` : ''}
      ${linksHtml ? `<div class="pub-links">${linksHtml}</div>` : ''}
    </div>
  `;
}

function renderPublications(pubs) {
  if (!pubList) return;
  pubList.innerHTML = pubs.map(createPubCard).join('');
}

// ===== Search + Filter Functionality =====
const searchInput = document.getElementById('pub-search-input');
let currentFilter = 'all';
let searchQuery = '';

function applyFilters() {
  const cards = pubList.querySelectorAll('.pub-card');
  cards.forEach(card => {
    const type = card.getAttribute('data-type');
    const cardText = card.textContent.toLowerCase();

    // Type filter
    let showByType = currentFilter === 'all' || type === currentFilter;

    // Search filter
    let showBySearch = !searchQuery || cardText.includes(searchQuery.toLowerCase());

    card.style.display = (showByType && showBySearch) ? '' : 'none';
  });
}

// Search input
if (searchInput) {
  searchInput.addEventListener('input', () => {
    searchQuery = searchInput.value.trim();
    applyFilters();
  });
}

// Filter buttons
filterBtns.forEach(btn => {
  btn.addEventListener('click', () => {
    filterBtns.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    currentFilter = btn.getAttribute('data-filter');
    applyFilters();
  });
});

// ===== Initialize =====
renderPublications(publications);
